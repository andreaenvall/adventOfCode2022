func DayTenPartOne(array []string) int {

	sum := []int{}
	cycle := 0
	value := 1
	for _, v := range array {
		splitted := strings.Split(v, " ")

		if splitted[0] == "noop" {
			cycle += 1
			if cycle == 20 || cycle == 60 || cycle == 100 || cycle == 140 || cycle == 180 || cycle == 220 {
				fmt.Println(cycle, value)
				ss := (cycle * value)
				sum = append(sum, ss)
			}

		} else if splitted[0] == "addx" {
			cycle += 1
			if cycle == 20 || cycle == 60 || cycle == 100 || cycle == 140 || cycle == 180 || cycle == 220 {
				fmt.Println(cycle, value)
				ss := (cycle * value)
				sum = append(sum, ss)
			}

			cycle += 1
			if cycle == 20 || cycle == 60 || cycle == 100 || cycle == 140 || cycle == 180 || cycle == 220 {
				// fmt.Println(v, num, cycle, value)
				ss := (cycle * value)
				sum = append(sum, ss)
			}
			num, _ := strconv.Atoi(splitted[1])
			value += num

		}

	}

	answer := 0
	for _, v := range sum {
		answer += v
	}
	return answer

}

